<p><strong>Sonderfall:</strong> Unternehmen mit einem Jahresumsatz von über <strong>800.000 €</strong> unterliegen voraussichtlich schon <strong>ab 2026</strong> der Verpflichtung zur elektronischen Rechnungsausstellung.</p>
<h2>Was bedeutet das für den Kanzleialltag?</h2>
<p>Viele Kanzleien arbeiten bereits digital – sei es über beA, DATEV-Export oder cloudbasierte Kanzleisoftware. Die Einführung der E-Rechnung bringt dennoch neue Anforderungen:</p>
<ul>
<li><strong>Rechnungsstellung:</strong> Rechnungen müssen künftig strukturiert elektronisch übermittelt werden.</li>
<li><strong>Buchhaltung:</strong> Eingangsrechnungen – z. B. von externen Dienstleistern – müssen digital verarbeitet und revisionssicher archiviert werden.</li>
<li><strong>Kommunikation:</strong> Die Zusammenarbeit mit Buchhaltung und Steuerberatung wird noch zentraler.</li>
</ul>
<p>Die Umstellung betrifft also nicht nur die Buchhaltung, sondern auch die <strong>Kanzleileitung, Assistenz und administrative Abläufe</strong>.</p>
<h2>Welche Schritte sollten Kanzleien jetzt einleiten?</h2>
<div class="blog-group">
