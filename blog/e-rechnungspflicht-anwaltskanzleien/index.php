<p class="has-background"><strong>Rechnungsadresse einrichten</strong>: <br>Eine zentrale E-Mail-Adresse für den Rechnungsempfang erleichtert die Organisation – z. B. <em><a>rechnungen@kanzleiname.de</a></em></p>
<p class="has-background"><strong>Mitarbeitende informieren</strong>: <br>Wer in der Kanzlei mit Rechnungen arbeitet, sollte die neuen Anforderungen kennen – inklusive Archivierungspflichten.</p>
