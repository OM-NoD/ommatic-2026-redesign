<p>? <strong>Bis Ende 2026 – Übergangsfrist</strong><br>Rechnungen dürfen weiterhin in Papierform oder als PDF versendet werden – jedoch <strong>nur mit ausdrücklicher Zustimmung des Empfängers</strong>.</p>
</div>
<div class="wp-block-column is-vertically-aligned-stretch has-background wp-block-column-is-layout-flow" style="background-color:#f2f2f2;padding-top:20px;padding-right:20px;padding-bottom:20px;padding-left:20px;flex-basis:33.33%">
<p>? <strong>Ab dem 1. Januar 2027</strong><br>Rechnungen an Unternehmen müssen verpflichtend <strong>elektronisch erstellt und übermittelt</strong> werden.</p>
</div>
</div>
