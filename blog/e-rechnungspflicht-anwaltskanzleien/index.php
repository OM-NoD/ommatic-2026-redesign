<p>Zum <strong>1. Januar 2025</strong> tritt eine neue gesetzliche Regelung in Kraft, die den Berufsalltag vieler Kanzleien – bewusst oder unbewusst – verändern wird: die Einführung und Umsetzung der <strong>elektronischen Rechnung im B2B-Bereich</strong>.</p>
<p>Die bevorstehenden Änderungen betreffen nicht nur große Kanzleien mit eigener IT-Abteilung, sondern auch kleine und mittelständische Kanzleien – <strong>unabhängig vom Vorsteuerabzugsrecht</strong>.</p>
<h2>Was ist eine elektronische Rechnung – und was wird künftig nicht mehr anerkannt?</h2>
<figure class="blog-figure">
<img src="https://ommatic.de/wp-content/uploads/2025/03/E-Rechnungspflicht-2025-Anwaelte-300x188.jpg" alt="E-Rechnungspflicht 2025 Anwälte" width="300" height="188" loading="lazy">
<figcaption><strong>E-Rechnungspflicht im Anmarsch:</strong><br>Was Kanzleien über die neue Gesetzeslage wissen sollten – verständlich erklärt.</figcaption></figure>
<p>Laut der neuen gesetzlichen Vorgabe handelt es sich bei einer elektronischen Rechnung <strong>nicht</strong> um ein PDF, das per E-Mail versendet wird, und auch <strong>nicht</strong> um eingescannte Dokumente. Entscheidend ist, dass die Rechnung <strong>eine elektronische Struktur besitzt</strong>, die <strong>automatisch verarbeitet</strong> werden kann.</p>
<p class="no-mb-p">Das bedeutet:</p>
<ul>
<li>maschinenlesbares Format</li>
<li>digitale Übermittlung</li>
<li>standardisierte Datenstruktur</li>
</ul>
<p class="no-mb-p">Ab dem Jahr 2027 gelten folgende Formate <strong>nicht mehr</strong> als rechtsgültige Rechnungen: </p>
<ul>
<li>einfache PDFs ohne strukturierte Daten</li>
<li>eingescannte Papierrechnungen</li>
<li>Word-Dokumente oder Bilddateien</li>
</ul>
<h2>Wen betrifft die neue Regelung?</h2>
<p>Die Verpflichtung gilt für <strong>alle Unternehmen mit Sitz in Deutschland</strong>, die Leistungen an andere Unternehmen erbringen – <strong>einschließlich Kanzleien, Notariate und Berufsausübungsgemeinschaften</strong>.</p>
<p>Auch Kanzleien, die gelegentlich Verträge mit Unternehmen abschließen oder Dienstleistungen von gewerblichen Anbietern beziehen, müssen sich auf die neuen Anforderungen vorbereiten – sowohl für das <strong>Erstellen</strong> als auch das <strong>Verarbeiten</strong> von Rechnungen.</p>
<h2>Zeitplan im Überblick</h2>
