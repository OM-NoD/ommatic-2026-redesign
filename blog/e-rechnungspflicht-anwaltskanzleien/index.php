<p>? <strong>Ab dem 1. Januar 2025</strong><br>Alle Unternehmen, einschließlich Kanzleien, müssen in der Lage sein, elektronische Rechnungen <strong>zu empfangen und zu verarbeiten</strong>.</p>
</div>
